package nominator.bean.vd;

public class NominatorDataBean {
	private int code;
	private String nominator;
	private String applicant;
	private String info;
	private int recruit_code;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getNominator() {
		return nominator;
	}
	public void setNominator(String nominator) {
		this.nominator = nominator;
	}
	public String getApplicant() {
		return applicant;
	}
	public void setApplicant(String applicant) {
		this.applicant = applicant;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public int getRecruit_code() {
		return recruit_code;
	}
	public void setRecruit_code(int recruit_code) {
		this.recruit_code = recruit_code;
	}	
	
}
